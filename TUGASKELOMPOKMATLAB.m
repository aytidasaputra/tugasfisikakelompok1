% --- Executes on button press in pushbutton4.
function pushbutton4_Callback(hObject, eventdata, handles)
q1 = str2num (get(handles.edit14, 'String'));
q2 = str2num (get(handles.edit15, 'String'));
q3 = str2num (get(handles.edit16, 'String'));
r1 = str2num (get(handles.edit17, 'String'));
r2 = str2num (get(handles.edit18, 'String'));

q1 = q1
q2 = q2
q3 = q3

k = 9*(10^9);
coad(90) = 0

% hitung gaya listrik 1
F12 = (k*q1*q2) / (r1^2);
% hitung gaya listrik 2
F13 = (k*q1*q3) / (r2^2);

% Menghitung resultan gaya
F1 = sqrt(F12)^2 + (F13)^2+2*(F13)*coad(90);

%Mneghitung medan listrik 1
E12 = (k*abs(q2)) / (r1^2);
%Mneghitung medan listrik 2
E13 = (k*abs(q3)) / (r2^2);

% Menghitung resultan medan listrik
F1 = sqrt(F12)^2 + (F13)^2+2*(F13)*coad(90);

%menghitung potensial listrik
v1 = (k*q1 /r1);

%menghitung usaha
W1 = v1 * q1


%validasi berdasarkan besaran

operasi = get(handles.popupmenu3, 'value');
if operasi == 1
    F12 = (k*q1*q2) / (r1^2);
elseif operasi == 2
    F13 = (k*q1*q3) / (r2^2);
elseif operasi == 3
    F1 = sqrt(F12)^2 + (F13)^2+2*(F13)*coad(90);
elseif operasi == 4
    E12 = (k*abs(q2)) / (r1^2);
elseif operasi == 5
    E13 = (k*abs(q3)) / (r2^2);
elseif operasi == 6
    E1 = sqrt(F12)^2 + (F13)^2+2*(F13)*coad(90);
elseif operasi == 7
    v1 = (k*q1 /r1);
elseif operasi == 8
    W1 = v1 * q1
end


%output/tampilkan
if operasi == 1
    set(handles.edit19, 'string', F12);
elseif operasi == 2
    set(handles.edit19, 'string', F13);
elseif operasi == 3
    set(handles.edit19, 'string', F1);
elseif operasi == 4
    set(handles.edit19, 'string', E12);
elseif operasi == 5
    set(handles.edit19, 'string', E13);
elseif operasi == 6
    set(handles.edit19, 'string', E1);
elseif operasi == 7
    set(handles.edit19, 'string', v1);
elseif operasi == 8
    set(handles.edit19, 'string', W1);
end
    
